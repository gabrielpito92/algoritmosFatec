for grupo in range(1, 3, 1):
  nome = input(f'Digite um nome para o grupo {grupo}:').upper()
  qtd = int(input(f'Digite a quantidade de elementos do grupo {grupo}:'))
  nomes_grupos.append(nome.strip(" "))
  qtd_grupos.append(qtd)

if total < (qtd_grupos[0] + qtd_grupos[1]):
  print('O número TOTAL de elementos é MENOR do que o número de elementos em cada grupo!!!')
else:
  intersec = int(total - (qtd_grupos[0] + qtd_grupos[1]))

  # Criando o diagrama de Venn
  venn = venn2(subsets=(qtd_grupos[0], qtd_grupos[1], intersec),
              set_labels=(nomes_grupos[0], nomes_grupos[1]))

  # Adicionando o título ao diagrama
  plt.title('Diagrama de Venn - Conjuntos')

  # Exibindo o diagrama
  plt.show()

  print ('_______________________________')
