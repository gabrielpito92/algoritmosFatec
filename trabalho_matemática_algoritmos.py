import matplotlib.pyplot as plt
from matplotlib_venn import venn2

# Dados fornecidos no exercício
programadores_java = 28
programadores_cobol = 12
programadores_java_cobol = 8

# Calculando a quantidade de programadores que só programam em JAVA
programadores_apenas_java = programadores_java - programadores_java_cobol

# Calculando a quantidade de programadores que só programam em COBOL
programadores_apenas_cobol = programadores_cobol - programadores_java_cobol

# Calculando a quantidade total de programadores
total_programadores = programadores_apenas_java + programadores_apenas_cobol + programadores_java_cobol

# Criando o diagrama de Venn
venn = venn2(subsets=(programadores_apenas_java, programadores_apenas_cobol, programadores_java_cobol),
             set_labels=('JAVA', 'COBOL'))

# Adicionando o título ao diagrama
plt.title('Diagrama de Venn - Equipe de Programadores')

# Exibindo o diagrama
plt.show()

print ('_______________________________')
print('Respostas:')
print('Existem', total_programadores, 'programadores na equipe.')
print('Existem', programadores_apenas_java,'programadores que utilizam somente Java.')

